#include "token.h"

extern size_t	hash_fnv1a(const char *s)
{
	size_t	h;

	h = 1469598103934665603ULL;
	while (*s)
	{
		h ^= (unsigned char)*s;
		h *= 1099511628211ULL;
		s++;
	}
	return (h);
}
