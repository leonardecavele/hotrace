#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
		if (((uint8_t *)s)[i] == (uint8_t)c)
			return (&((uint8_t *)s)[i]);
	return (0);
}
